If Arsenal are 'evens' to win at home to Everton and the correct score odds for Arsenal to win 1-0 is 8/1, by having the double Arsenal to win and the correct score to be 1-0, a customer would be getting 17/1 about an 8/1 chance, for if Arsenal win 1-0 they would automatically have won the match.

If such a bet is taken in error it will be settled as two single bets with the stake equally divided.

An exception to this rule is where special odds are offered for the combined eventuality, e.g. First Player to Score/Correct Score Double, known as a Scorecast or where a customer has combined interrelated bets into one bet using Bet Builder.

When the related parts of a bet are resolved at different times but still within the same tournament, the bet will be settled as instructed, with the price of the second or subsequent selection being determined at each individual stage.

For example, a double selects:

Federer to win the semi final of Wimbledon; and

Federer to win the Wimbledon tournament outright.

As the odds of the second selection depends on the outcome of the first, the odds of Roger Federer winning the tournament at the time the bet was struck cannot be used to calculate the double, and the correct odds after the initial selection is completed will be attributed instead.

Multiple bets which combine different selections within different events are also not accepted where the outcome of one affects or is affected by the other.

For example, a double selecting:

King's Best to win the 2000 Guineas; and

King's Best to win the Derby.

As the odds for King's Best to win the Derby depend on whether he wins the 2000 Guineas, the odds for him to win the Derby at the time the bet was struck cannot be used to calculate the double. A special price to win both races will be quoted instead.

In the case of all multiples including selections with related outcomes, if a bet is taken inadvertently bet365 reserves the right to void part of or the whole of the bet.

2-Way Markets

In the event of a tie in a 2-Way market, where no price is offered for the tie, bets will be settled according to the individual sports rules.

Dead-Heats

Where selections dead-heat, the stake money is proportioned according to the number of selections which dead-heat and paid at full-odds.

Any-to-come (also known as ATC or 'if cash' or 'if' bets)

ATC (if) bets are accepted win only on all sports. You can choose to carry forward the same stake unit or a specific part of your potential returns from one selection to the next as we proceed through your list of ATC bets. ATC bets are settled in the order that they appear on your betting slip, until all the money set aside has been used. If the money staked (or subsequently carried forward) onto the next selection is insufficient to follow your instructions, then that selection will be settled in proportion to the amount available.

Please note that your ATC (if) stake must not exceed the stake on your previous selection.

Reverse any-to-come (also known as reverse ATC or 'reverse if cash' or 'reverse if' bets)

You may select any number of selections (maximum 20) in two reversed ATC (if) bets. The above ATC settlement rules apply, but the stake unit will remain the same throughout the bet. For the reverse order part of your bet, the stake from any returns from the last selection on your slip will go onto the penultimate selection etc (i.e. bets are settled in the reverse order they appear on your betting slip).

For all ATC and reverse ATC bets, customers have the choice when placing their bet of either Single Action or Double Action, i.e. the customer may choose to stop the bet at a void or Non-Runner.

Rule 4 general sports

Outright markets (defined as after the draw/official field is confirmed for a specific event), may be subject to a deduction equivalent to the Rule 4 (Deductions). The Rule 4 will be applied, according to the table of deductions (a table of deductions, and further Rule 4 rules related to Horse Racing can be found within the Horse Racing Rules section); with the exception that a Rule 4 of 5 pence will not be enforced. Effectively the Rule 4 will be applied, in the case of participant withdrawals, with odds of 9/1 or smaller. Deductions may apply for any runners scratched from the Final Field prior to race commencement. Any such deductions will apply to the total payout value on all bets placed prior to the scratching and the reframing of a new market by bet365 (a table of deductions, related to Horse Racing can be found within the Australasian Rules section).

